"""
《绝地求生》游戏中，护甲和防弹衣都有三种等级，分别记为1,2,3. 每次你只能捡起没有的装备，或者将低等级的装备升级到高等级，
问从什么都没有到“三级护甲三级防弹衣”，有多少种升级路线？用有序数对（防弹衣，护甲）来表示当前状态，二者取值0-3，
比如（0,0）->（0,1）->（0,3）->（3,3）为一种升级策略
"""
if __name__ == '__main__':
	dp = [[0, 0, 0, 0] for i in range(4)]
	dp[0][0] = 1
	for i in range(4):
		for j in range(4):
			for m in range(i):
				dp[i][j] += dp[m][j]
			for n in range(j):
				dp[i][j] += dp[i][n]
	print(dp[3][3])
